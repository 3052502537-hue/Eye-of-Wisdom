/* ============================================================
 *  文件名: camera_driver.h
 *  功能描述:
 *    导盲头环项目 - OV2640 摄像头驱动头文件
 *    定义摄像头驱动类 CameraDriver，封装 OV2640 的初始化、
 *    图像采集、JPEG 压缩参数设置、图像简单处理等功能。
 *
 *  依赖关系:
 *    - config.h (引脚和参数定义)
 *    - esp_camera.h (ESP32 官方摄像头库，随 ESP32 Arduino 库提供)
 *    - Arduino.h (Arduino 核心库)
 *
 *  接口说明:
 *    CameraDriver 类提供以下接口:
 *      init()           - 初始化摄像头
 *      capture()        - 采集一帧图像
 *      returnFrame()    - 释放帧缓冲区
 *      setResolution()  - 设置分辨率
 *      setQuality()     - 设置 JPEG 压缩质量
 *      setBrightness()  - 设置亮度
 *      setContrast()    - 设置对比度
 *      setSaturation()  - 设置饱和度
 *      getFrameInfo()   - 获取当前帧信息
 *      isInitialized()  - 查询初始化状态
 *
 *  采集流程:
 *    init() → capture() → 处理图像 → returnFrame() → 循环
 * ============================================================ */

#ifndef CAMERA_DRIVER_H
#define CAMERA_DRIVER_H

#include <stdint.h>
#include <stdbool.h>
#include "Arduino.h"
#include "esp_camera.h"

/* ============================================================
 *  图像帧信息结构体
 *    描述一帧图像的元数据
 * ============================================================ */
typedef struct {
    uint16_t width;         /* 图像宽度 (像素) */
    uint16_t height;        /* 图像高度 (像素) */
    uint8_t  format;        /* 像素格式 (PIXFORMAT_JPEG 等) */
    uint32_t size;          /* 数据大小 (字节) */
    uint32_t timestamp;     /* 采集时间戳 (ms) */
} FrameInfo_t;

/* ============================================================
 *  摄像头驱动类
 *    封装 OV2640 摄像头的所有操作
 * ============================================================ */
class CameraDriver {
public:
    /* 构造函数 */
    CameraDriver();

    /* 析构函数，自动释放摄像头资源 */
    ~CameraDriver();

    /* --------------------------------------------------------
     *  初始化 OV2640 摄像头
     *  参数:
     *    无 (引脚从 config.h 读取)
     *  返回值:
     *    true  - 初始化成功
     *    false - 初始化失败
     *  说明:
     *    配置 DVP 引脚、VGA 分辨率、JPEG 输出、双缓冲等
     *    初始化成功后即可调用 capture() 采集图像
     * -------------------------------------------------------- */
    bool init(void);

    /* --------------------------------------------------------
     *  采集一帧图像
     *  参数:
     *    fb - 输出参数，返回摄像头帧缓冲区指针
     *  返回值:
     *    true  - 采集成功，fb 指向有效帧数据
     *    false - 采集失败
     *  说明:
     *    采集后通过 fb->buf 访问图像数据，fb->len 为数据长度
     *    使用完毕后必须调用 returnFrame() 释放缓冲区
     * -------------------------------------------------------- */
    bool capture(camera_fb_t** fb);

    /* --------------------------------------------------------
     *  释放帧缓冲区
     *  参数:
     *    fb - 要释放的帧缓冲区指针
     *  返回值: 无
     *  说明:
     *    采集到的帧用完后必须释放，否则会耗尽缓冲区
     * -------------------------------------------------------- */
    void returnFrame(camera_fb_t* fb);

    /* --------------------------------------------------------
     *  设置分辨率
     *  参数:
     *    size - 帧尺寸枚举 (FRAMESIZE_VGA, FRAMESIZE_QVGA 等)
     *  返回值:
     *    true  - 设置成功
     *    false - 设置失败或未初始化
     * -------------------------------------------------------- */
    bool setFrameSize(framesize_t size);

    /* --------------------------------------------------------
     *  设置 JPEG 压缩质量
     *  参数:
     *    quality - 质量等级 (5-63，数值越小质量越高、文件越大)
     *  返回值:
     *    true  - 设置成功
     *    false - 设置失败
     * -------------------------------------------------------- */
    bool setQuality(int quality);

    /* --------------------------------------------------------
     *  设置亮度
     *  参数: level - 亮度等级 (-2 到 +2)
     *  返回值: true=成功, false=失败
     * -------------------------------------------------------- */
    bool setBrightness(int level);

    /* --------------------------------------------------------
     *  设置对比度
     *  参数: level - 对比度等级 (-2 到 +2)
     *  返回值: true=成功, false=失败
     * -------------------------------------------------------- */
    bool setContrast(int level);

    /* --------------------------------------------------------
     *  设置饱和度
     *  参数: level - 饱和度等级 (-2 到 +2)
     *  返回值: true=成功, false=失败
     * -------------------------------------------------------- */
    bool setSaturation(int level);

    /* --------------------------------------------------------
     *  翻转图像 (水平/垂直镜像)
     *  参数:
     *    hmirror - true=水平翻转, false=正常
     *    vflip   - true=垂直翻转, false=正常
     *  返回值: true=成功, false=失败
     *  说明: 导盲头环安装方向可能需要翻转图像
     * -------------------------------------------------------- */
    bool setFlip(bool hmirror, bool vflip);

    /* --------------------------------------------------------
     *  获取当前帧信息
     *  参数: info - 输出参数，返回帧信息
     *  返回值: true=成功, false=未初始化
     * -------------------------------------------------------- */
    bool getFrameInfo(FrameInfo_t* info);

    /* 内联访问器函数 */
    int getWidth(void) const { return _width; }        /* 获取当前宽度 */
    int getHeight(void) const { return _height; }      /* 获取当前高度 */
    bool isInitialized(void) const { return _initialized; }  /* 查询初始化状态 */
    uint32_t getFrameCount(void) const { return _frameCount; }  /* 获取累计帧数 */

private:
    bool      _initialized;     /* 初始化状态标志 */
    int       _width;           /* 当前图像宽度 */
    int       _height;          /* 当前图像高度 */
    pixformat_t _pixelFormat;   /* 当前像素格式 */
    uint32_t  _frameCount;      /* 累计采集帧数 */

    /* --------------------------------------------------------
     *  内部: 配置摄像头硬件参数
     *  根据config.h的引脚定义填充 camera_config_t 结构
     *  返回值: ESP_OK=成功, 其他=失败
     * -------------------------------------------------------- */
    esp_err_t _configHardware(void);
};

#endif /* CAMERA_DRIVER_H */
